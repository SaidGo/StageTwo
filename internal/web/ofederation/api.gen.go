type LegalEntity struct {
	Address *string `json:"address,omitempty"`
	Inn     *string `json:"inn,omitempty"`
	Kpp     *string `json:"kpp,omitempty"`
	Name    *string `json:"name,omitempty"`
	Ogrn    *string `json:"ogrn,omitempty"`
	Uuid    *string `json:"uuid,omitempty"`
}


type ServerInterface interface {
	
	
	GetLegalEntities(w http.ResponseWriter, r *http.Request)
}



type Unimplemented struct{}



func (_ Unimplemented) GetLegalEntities(w http.ResponseWriter, r *http.Request) {
	w.WriteHeader(http.StatusNotImplemented)
}


type ServerInterfaceWrapper struct {
	Handler            ServerInterface
	HandlerMiddlewares []MiddlewareFunc
	ErrorHandlerFunc   func(w http.ResponseWriter, r *http.Request, err error)
}

type MiddlewareFunc func(http.Handler) http.Handler


func (siw *ServerInterfaceWrapper) GetLegalEntities(w http.ResponseWriter, r *http.Request) {
	ctx := r.Context()

	handler := http.Handler(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		siw.Handler.GetLegalEntities(w, r)
	}))

	for _, middleware := range siw.HandlerMiddlewares {
		handler = middleware(handler)
	}

	handler.ServeHTTP(w, r.WithContext(ctx))
}

